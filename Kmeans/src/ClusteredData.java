public class ClusteredData {
    public double x;
    public double y;
    public int centroide;

   

    public ClusteredData(double x, double y, int centroide) {
        this.x = x;
        this.y = y;
        this.centroide = centroide;
    }

    public double getx(){
        return x;
    }
    public double gety(){
        return y;
    }
    public int getCentroide(){
        return centroide;
    }
    

}
